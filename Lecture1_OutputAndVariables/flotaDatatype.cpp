#include<iostream>
using namespace std;
int main(){
    float x = 3 , y = 6;
    float z = x/y;
    cout<<"3.0/6.0 is "<<z<<endl;
    cout<<"3/6 is "<<3/6<<endl;
// The Computer Automatically Stores The Result As Integer Datatype. So Output = 0
    cout<<"3.0/6 is "<<3.0/6<<endl;
    cout<<"3/6.0 is "<<3/6.0<<endl;
    cout<<endl<<"Output of = 5.0 In Cout Command is "<<5.0<<endl;
// Any Operation Between Two Float Values Or Between Integer And Float Values Will Always Return A Float Result.
}
